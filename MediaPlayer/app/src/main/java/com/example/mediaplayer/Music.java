package com.example.mediaplayer;

public class Music {
    private String musicName;
    private int id;

    public Music(){

    }

    public String getMusicName() {
        return musicName;
    }

    public void setMusicName(String musicName) {
        this.musicName = musicName;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
